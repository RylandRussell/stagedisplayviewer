package se.pingstteknik.propresenter.stagedisplayviewer.util;

/**
 * @version 1.7
 * @since 1.7
 */
public class CapitalizeTextTranslator {

    /**
     * Capitalizes text.
     *
     * @param text
     * @return Capitalized text
     */
    public static String transform(String text) {
        return text.toUpperCase();
    }
}
